int	ft_sqrt(int nb)
{
	int i;
	int nbr;

	i = 1;
	nbr = 0;
	while (i * i <= nb)
	{
		nbr = i * i;
		i++;
	}
	if (nbr == nb)
	{
		return (i - 1);
	}
	else
	{
		return (0);
	}
}
