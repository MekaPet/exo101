char	*ft_strcapitalize(char *str)
{
	int		i;
	char	*c_c;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] = str[i] + 32;
		if ((str[i] >= 97 && str[i] <= 123) &&
		(str[i - 1] == ' ' || str[i - 1] == '-' || str[i - 1] == '+'))
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
